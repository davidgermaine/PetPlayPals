package com.techelevator.model;

public class Playdate {
	
	private int playdateId;
	private String username;
	private String playdateDate;
	private String address;
	private String city;
	private String region;
	private String zipcode;
	private String longitude;
	private String latitude;
	private String playdateTime;
	
	
	public int getPlaydateId() {
		return playdateId;
	}
	
	public void setPlaydateId(int playdateId) {
		this.playdateId = playdateId;
	}
	
	public String getUsername() {
		return username;
	}
	
	public void setUsername(String username) {
		this.username = username;
	}
	
	public String getPlaydateDate() {
		return playdateDate;
	}
	
	public void setPlaydateDate(String playdateDate) {
		this.playdateDate = playdateDate;
	}
	

	
	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public String getCity() {
		return city;
	}

	public void setCity(String city) {
		this.city = city;
	}

	public String getRegion() {
		return region;
	}

	public void setRegion(String region) {
		this.region = region;
	}

	public String getZipcode() {
		return zipcode;
	}

	public void setZipcode(String zipcode) {
		this.zipcode = zipcode;
	}
	

	public String getLongitude() {
		return longitude;
	}

	public void setLongitude(String longitude) {
		this.longitude = longitude;
	}

	public String getLatitude() {
		return latitude;
	}

	public void setLatitude(String latitude) {
		this.latitude = latitude;
	}

	public String getPlaydateTime() {
		return playdateTime;
	}
	
	public void setPlaydateTime(String playdateTime) {
		this.playdateTime = playdateTime;
	}

	
}
