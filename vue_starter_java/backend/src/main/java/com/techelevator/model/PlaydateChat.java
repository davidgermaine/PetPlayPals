package com.techelevator.model;

public class PlaydateChat {
	
	private int playdateChatId;
	private int playdateId;
	private String username;
	private String message;
	private String date;
	
	public int getPlaydateChatId() {
		return playdateChatId;
	}
	public void setPlaydateChatId(int playdateChatId) {
		this.playdateChatId = playdateChatId;
	}
	public int getPlaydateId() {
		return playdateId;
	}
	public void setPlaydateId(int playdateId) {
		this.playdateId = playdateId;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getMessage() {
		return message;
	}
	public void setMessage(String message) {
		this.message = message;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	
	

}
